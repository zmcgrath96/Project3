public static class GameStats
{

    private static int level;
    private static float levelDuration;

    public static int Level
    {
        get
        {
            return level;
        }
        set
        {
            level = Level;
        }
    }

    public static float LevelDuration
    {
        get
        {
            return levelDuration;
        }
        set
        {
            levelDuration = LevelDuration;
        }
    }
}
